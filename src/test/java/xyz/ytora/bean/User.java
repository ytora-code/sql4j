package xyz.ytora.bean;

public class User {
    private String userName;
    private String userEmail;
    private Integer age;
    private Long id;

    // Getter方法
    public String getUserName() {
        return userName;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public Integer getAge() {
        return age;
    }

    public Long getId() {
        return id;
    }
}
